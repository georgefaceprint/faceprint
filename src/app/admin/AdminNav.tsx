export default function AdminNav() {
  const pathname = usePathname();

  const isActive = (href: string, exact?: boolean) => {
    if (exact) return pathname === href;
    return pathname.startsWith(href);
  };

  return (
    <nav className="flex-1 p-4 space-y-1 overflow-y-auto">
      {/* Main nav items */}
      {navItems.map(item => {
        const active = isActive(item.href, item.exact);
        return (
          <Link
            key={item.href}
            href={item.href}
            className={`flex items-center gap-3 px-3 py-2.5 rounded-xl text-sm font-medium transition-all ${
              active
                ? 'bg-gradient-to-r from-purple-600/30 to-cyan-600/10 text-white border border-purple-500/30 shadow-[0_0_12px_rgba(139,92,246,0.15)]'
                : 'text-gray-400 hover:text-white hover:bg-[rgba(255,255,255,0.06)]'
            }`}
          >
            <span className={active ? 'text-purple-400' : ''}>{item.icon}</span>
            {item.label}
            {active && (
              <span className="ml-auto w-1.5 h-1.5 rounded-full bg-purple-400" />
            )}
          </Link>
        );
      })}

      {/* Divider */}
      <div className="pt-4 pb-1">
        <p className="text-[10px] font-bold text-gray-600 uppercase tracking-widest px-3">Quick Actions</p>
      </div>

      {/* Action items */}
      {divider.items.map(item => {
        const active = isActive(item.href);
        return (
          <Link
            key={item.href}
            href={item.href}
            className={`flex items-center gap-3 px-3 py-2.5 rounded-xl text-sm font-semibold transition-all ${
              active
                ? 'bg-purple-600 text-white shadow-[0_0_15px_rgba(139,92,246,0.4)]'
                : 'bg-purple-600/10 text-purple-400 border border-purple-500/20 hover:bg-purple-600/20 hover:text-purple-300'
            }`}
          >
            {item.icon}
            {item.label}
          </Link>
        );
      })}
    </nav>
  );
}
